<template>
  <div class="order-list">
    <div class="wrapper">
      <div class="container">
        <div class="order-box">
          <!-- <loading v-show="loading" /> -->

          <!-- 订单 -->
          <div class="order" v-for="(order,index) in list" :key="index">

            <!-- 标题 -->
            <div class="order-title">
              <div class="item-info fl">
                {{order.createTime}}
                <span>|</span>
                {{order.receiverName}}
                <span>|</span>
                订单号：{{order.orderNo}}
                <span>|</span>
                {{order.paymentTypeDesc}}
              </div>
              <div class="item-money fr">
                <span>应付金额：</span>
                <span class="money">{{order.payment}}</span>
                <span>元</span>
              </div>
            </div>

            <!-- 内容 -->
            <div class="order-content clearfix">
              <div class="good-box fl">
                <div class="good-list" v-for="(item,i) in order.orderItemVoList" :key="i">
                  <div class="good-img">
                    <img v-lazy="item.productImage" alt="">
                  </div>
                  <div class="good-name">
                    <div class="p-name">{{item.productName}}</div>
                    <div class="p-money">{{ `${item.totalPrice} x${item.quantity} 元` }}</div>
                  </div>
                </div>
              </div>
              <div class="good-state fr" v-if="order.status == 20">
                <a href="javascript:;">{{order.statusDesc}}</a>
              </div>
              <div class="good-state fr" v-else>
                <a href="javascript:;" @click="goPay(order.orderNo)">{{order.statusDesc}}</a>
              </div>
            </div>

          </div>

          <!-- 没有数据时显示 -->
          <no-data v-show="!loading && list.length == 0"></no-data>

          <!-- 分页器 -->
          <!-- <el-pagination background layout="prev, pager, next" :total="total" :page-size="pageSize"
            @current-change="handleChange" /> -->

          <!-- 无限滚动加载更多 -->
          <div class="scroll-more" v-infinite-scroll="scrollMore" :infinite-scroll-disabled="busy"
            infinite-scroll-distance="410">
            <!-- 加载的时候放个动画 -->
            <img src="/imgs/loading-svg/loading-spinning-bubbles.svg" alt="" v-show="loading">
          </div>
·
        </div>
      </div>
    </div>
  </div>
</template>
<script>
import Loading from '@/components/Loading.vue';
import NoData from '@/components/NoData.vue';
import infiniteScroll from 'vue-infinite-scroll' 

export default {
  components: {
    Loading,
    NoData,
  },
  directives: { // vue-infinite-scroll无限滚动插件
    infiniteScroll
  },
  data() {
    return {
      list: [],
      loading: false,
      pageSize: 5,
      pageNum: 1,
      total: 0,
      busy: false, // 是否禁用触发滚动加载（
    }
  },
  mounted() {
    this.getOrderList();
  },
  methods: {
    getOrderList() {
      this.loading = true
      this.busy = true // 页面一打开先关闭无限滚动加载更多
      this.$axios.get('/orders', {
        params: {
          pageSize: this.pageSize,
          pageNum: this.pageNum
        }
      })
        .then(res => {
          this.loading = false
          this.list = res.list
          this.total = res.total
          this.total = false // 请求完在打开
        })
        .catch(err => {
          this.loading = false
          console.log(err)
        })
    },
    goPay(orderNo) {
      this.$router.push({
        path: '/order/pay',
        query: {
          orderNo
        }
      })
    },
    handleChange(pageNum) {
      this.pageNum = pageNum
      this.getOrderList()
    },
    // 滚动加载更多
    scrollMore() {
      this.busy = true // 禁用无限滚动上拉加载更多
      this.loading = true // 打开动画
      setTimeout(() => {
        this.pageNum++ // 当前页数 加一
        this.getList() // 得到列表
        this.busy = false // 开启无限滚动上拉加载更多
        this.loading = false // 关闭动画
      }, 1000);
    },
    // 专门给 scrollMore使用
    getList() {
      this.$axios.get('/orders', {
        params: {
          pageSize: this.pageSize,
          pageNum: this.pageNum
        }
      })
        .then(res => {
          this.list = this.list.concat(res.list) // 不断给列表 push，不刷新页面
          if (res.hasNextPage) return this.busy = false // 如果接口有下一页
          this.busy = true
        })
    },
  }
}
</script>
